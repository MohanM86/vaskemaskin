export function getKommuneBySlug(slug: string): Kommune | undefined {
  return kommuner.find(function(k) { return k.slug === slug })
}

export function getKommunerByFylke(fylkeSlug: string): Kommune[] {
  return kommuner.filter(function(k) { return k.fylkeSlug === fylkeSlug })
}

export function searchKommuner(query: string): Kommune[] {
  var q = query.toLowerCase()
  return kommuner.filter(function(k) {
    return k.name.toLowerCase().indexOf(q) !== -1
  })
}